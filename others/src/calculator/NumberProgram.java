package logical;

public class NumberSeries {
	
	public static void main(String[] args) {
		
		System.out.println("Number are classified into 1. Real Numbers 2. Complex Numbers");
		
		System.out.println("Rational Integers Whole Natural Irrational Numbers are into Real Numbers");
		
		System.out.println("Number with limited decimal value is defined as rational number.");
		
		System.out.println("Example : 0.1, 0.2, 0.3, 0.4");
		
		System.out.println("Number which not can be expressed with limited decimal fractions is irrational number");
		
		System.out.println("Example : pi, √2, √3, √5");
		
		System.out.println("All the negative and positive numbers with no decimal fraction values are integers");
		
		System.out.println("Example : -1, -2, 0, 1, 2");
		
		System.out.println("0 to n all the positive numbers with no decimal fraction values are whole numbers");
		
		System.out.println("Example : 0, 1, 2, 3, 4, 5");
		
		System.out.println("1 to n all the positive numbers with no decimal fraction values are natural numbers");
		
		System.out.println("Example : 1, 2, 3, 4, 5, 6");
		
		System.out.println("Complex numbers are in the form of a+ib where a and b are real numbers and i is an imaginary number");
		
		System.out.println("Example : 1+2i, 2+3i, 3+4i, 4+5i");
		
	}

}
